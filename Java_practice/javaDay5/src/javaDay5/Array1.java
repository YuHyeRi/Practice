package javaDay5;

public class Array1 {
	
	public static void main(String[] args) {
		
		// 배열1
		String[] fruit = {"딸기", "포도"};
		System.out.println(fruit[0]);
		System.out.println(fruit[1]);
		
		// 배열2
		String[] fruit2 = new String[2];
		fruit2[0] = "1";
		fruit2[1] = "2";
		System.out.println(fruit2[0]);
		System.out.println(fruit2[1]);
		
	}
}
